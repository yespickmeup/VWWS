    private void ok() {
        double previous = FitIn.toDouble(tf_fname.getText());
        double current = FitIn.toDouble(tf_fname1.getText());
        if (current < previous) {
            return;
        }
        String previous_reading_date = tf_fname2.getText();
        if (previous_reading_date.isEmpty() || previous_reading_date.equalsIgnoreCase("none")) {
            previous_reading_date = "1991-02-08 00:00:00";
        } else {
            try {
                Date d = DateType.slash.parse(previous_reading_date);
                previous_reading_date = DateType.datetime.format(d);
            } catch (ParseException ex) {
                Logger.getLogger(Dlg_readings_add.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(previous, current, previous_reading_date));
        }
    }
