    Readings.to_readings reading = null;

    public void do_pass(Readings.to_readings reading1) {
        reading = reading1;
        jTextField2.setText(FitIn.fmt_woc(reading.previous_reading));
        jTextField3.setText(FitIn.fmt_woc(reading.current_reading));
        jTextField5.setText(FitIn.fmt_woc(reading.mf));

        jTextField6.setText(FitIn.fmt_woc(reading.mr));
        jTextField8.setText(FitIn.fmt_wc_0(reading.interest));
        jTextField7.setText(FitIn.fmt_wc_0(reading.discount));
        jTextField9.setText(FitIn.fmt_wc_0(reading.net_due));

        Date d1 = new Date();
        Date d2 = new Date();
        try {
            d1 = DateType.sf.parse(reading1.previous_reading_date);
            d2 = DateType.sf.parse(reading1.created_at);
            jDateChooser1.setDate(d1);
        } catch (ParseException ex) {
            Logger.getLogger(Dlg_reading_overide_input.class.getName()).log(Level.SEVERE, null, ex);
        }

        count_consumption();

    }

