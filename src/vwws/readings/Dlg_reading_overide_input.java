    private void count_consumption() {
        double previous_reading = FitIn.toDouble(jTextField2.getText());
        double current_reading = FitIn.toDouble(jTextField3.getText());
        double total_consumption = current_reading - previous_reading;
        jTextField4.setText(FitIn.fmt_woc(total_consumption));
    }

    private void ok() {

        double previous_reading = FitIn.toDouble(jTextField2.getText());
        double current_reading = FitIn.toDouble(jTextField3.getText());
        double total_consumption = FitIn.toDouble(jTextField4.getText());
        double mf = FitIn.toDouble(jTextField5.getText());
        double mr = FitIn.toDouble(jTextField6.getText());
        double interest = FitIn.toDouble(jTextField8.getText());
        double discount = FitIn.toDouble(jTextField7.getText());
        double net_due = FitIn.toDouble(jTextField9.getText());
        String previous_date = DateType.sf.format(jDateChooser1.getDate());
        String current_date = DateType.datetime.format(jDateChooser2.getDate());

        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(previous_reading, current_reading, total_consumption, mf, mr, interest, discount, net_due, previous_date, current_date));
        }
    }
