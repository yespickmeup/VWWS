    private void init_report() {

        jProgressBar1.setString("Loading...Please wait...");
        jProgressBar1.setIndeterminate(true);
        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {
                Field.Combo city = (Field.Combo) jTextField1;
                Field.Combo barangay = (Field.Combo) jTextField2;
                Field.Combo purok = (Field.Combo) jTextField3;
                Field.Combo sitio = (Field.Combo) jTextField4;

                String where = " where discount_customer_name like '%%' ";
                if (!jCheckBox1.isSelected()) {
                    where = where + " and city_id='" + city.getId() + "' ";
                    if (!jCheckBox2.isSelected()) {
                        where = where + " and barangay_id='" + barangay.getId() + "' ";
                        if (!jCheckBox4.isSelected()) {
                            where = where + " and purok_id='" + purok.getId() + "' ";
                            if (!jCheckBox3.isSelected()) {
                                where = where + " and sitio_id='" + sitio.getId() + "' ";
                            }
                        }
                    }
                }
                String search = jTextField7.getText();
                if (jCheckBox6.isSelected()) {

                    if (!search.isEmpty()) {
                        String where2 = where.replace("where", "");
                        where = where + " and (concat(fname,'',lname)) like '%" + search + "%' or " + where2 + " and (concat(lname,'',fname)) like '%" + search + "%'";

                    }

                } else if (jCheckBox9.isSelected()) {
                    where = where + " and meter_no like '%" + search + "%' ";
                } else if (jCheckBox10.isSelected()) {
                    where = where + " and customer_no like '%" + search + "%' ";
                }

                if (jCheckBox12.isSelected()) {
                    where = where + " and status=0 ";
                } else if (jCheckBox13.isSelected()) {
                    where = where + " and status=1";
                }

                String date_from = DateType.sf.format(jDateChooser1.getDate());
                String date_to = DateType.sf.format(jDateChooser2.getDate());
                if (!jCheckBox7.isSelected()) {
                    where = where + " and Date(date_added) between '" + date_from + "' and '" + date_to + "' ";
                }

                where = where + " order by or_no asc ";
                System.out.println(where);
                List<Rpt_collections.field> fields = Rpt_collections.ret_data(where);
                Rpt_collections rpt = new Rpt_collections();
                rpt.fields.addAll(fields);
                String jrxml = "rpt_collections.jrxml";
                report_sales_items(rpt, jrxml);

                InputStream is = Rpt_collections.class.getResourceAsStream(jrxml);
                try {
                    JasperReport jasperReport = JasperCompileManager.compileReport(is);
                    jasperPrint = JasperFillManager.fillReport(jasperReport, JasperUtil.
                                                               setParameter(rpt), JasperUtil.makeDatasource(rpt.fields));

                } catch (JRException ex) {
                    Logger.getLogger(Dlg_report_customers.class.getName()).
                            log(Level.SEVERE, null, ex);
                }
                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);

            }
        });
        t.start();

    }

    private void report_sales_items(final Rpt_collections to, String jrxml_name) {
        pnl_report.removeAll();
        pnl_report.setLayout(new BorderLayout());
        try {
            JRViewer viewer = get_viewer_expenses(to, jrxml_name);
            JPanel pnl = new JPanel();
            pnl.add(viewer);
            pnl.setVisible(true);
            pnl.setVisible(true);
            pnl_report.add(viewer);
            pnl_report.updateUI();

        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static JRViewer get_viewer_expenses(Rpt_collections to, String rpt_name) {
        try {
            return JasperUtil.getJasperViewer(
                    compileJasper(rpt_name),
                    JasperUtil.setParameter(to),
                    JasperUtil.makeDatasource(to.fields));
        } catch (Exception e) {
            throw new RuntimeException(e);
        } finally {
        }
    }

    public static JasperReport compileJasper(String rpt_name) {
        try {
            String jrxml = rpt_name;
            InputStream is = Rpt_collections.class.getResourceAsStream(jrxml);
            JasperReport jasper = JasperCompileManager.compileReport(is);
            return jasper;
        } catch (JRException e) {
            throw new RuntimeException(e);
        }
    }
    JasperPrint jasperPrint = null;

    private void print() {
        try {
            if (jasperPrint != null) {
                JasperPrintManager.printReport(jasperPrint, false);
            }

        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, "Failed To Print, Please Check the Printer");
            throw new RuntimeException(e);
        }
    }

