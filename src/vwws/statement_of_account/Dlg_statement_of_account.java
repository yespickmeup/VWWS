    String tax_declaration_no = "";
    String water_meter_no = "";

    private void init_report() {

        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {
                Field.Search tf = (Field.Search) tf_fname;
                String where = " where customer_id='" + tf.getId() + "' and is_paid='" + "0" + "' ";
                List<Rpt_statement_of_account.field> fields = Rpt_statement_of_account.ret_data(where);
                Rpt_statement_of_account rpt = new Rpt_statement_of_account(barangay, date_of_billing, due_date, customer_name, tax_declaration_no, water_meter_no);
                rpt.fields.addAll(fields);
                String jrxml = "rpt_statement_of_account.jrxml";
                report_sales_items(rpt, jrxml);

                InputStream is = Rpt_statement_of_account.class.getResourceAsStream(jrxml);
                try {
                    JasperReport jasperReport = JasperCompileManager.compileReport(is);
                    jasperPrint = JasperFillManager.fillReport(jasperReport, JasperUtil.
                            setParameter(rpt), JasperUtil.makeDatasource(rpt.fields));

                } catch (JRException ex) {
                    Logger.getLogger(Dlg_report_customers.class.getName()).
                            log(Level.SEVERE, null, ex);
                }

            }
        });
        t.start();

    }

    private void report_sales_items(final Rpt_statement_of_account to, String jrxml_name) {
        pnl_report.removeAll();
        pnl_report.setLayout(new BorderLayout());
        try {
            JRViewer viewer = get_viewer_expenses(to, jrxml_name);
            JPanel pnl = new JPanel();
            pnl.add(viewer);
            pnl.setVisible(true);
            pnl.setVisible(true);
            pnl_report.add(viewer);
            pnl_report.updateUI();

        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static JRViewer get_viewer_expenses(Rpt_statement_of_account to, String rpt_name) {
        try {
            return JasperUtil.getJasperViewer(
                    compileJasper(rpt_name),
                    JasperUtil.setParameter(to),
                    JasperUtil.makeDatasource(to.fields));
        } catch (Exception e) {
            throw new RuntimeException(e);
        } finally {
        }
    }

    public static JasperReport compileJasper(String rpt_name) {
        try {
            String jrxml = rpt_name;
            InputStream is = Rpt_statement_of_account.class.getResourceAsStream(jrxml);
            JasperReport jasper = JasperCompileManager.compileReport(is);
            return jasper;
        } catch (JRException e) {
            throw new RuntimeException(e);
        }
    }
    JasperPrint jasperPrint = null;

    private void print() {
        try {
            if (jasperPrint != null) {
                JasperPrintManager.printReport(jasperPrint, false);
            }

        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, "Failed To Print, Please Check the Printer");
            throw new RuntimeException(e);
        }
    }

    List<Customers.to_customers> customers = new ArrayList();

    private void init_customers() {
        Object[][] obj = new Object[customers.size()][1];
        int i = 0;
        for (Customers.to_customers to : customers) {
            obj[i][0] = " " + to.lname + ", " + to.fname + " " + to.mi;
            i++;
        }

        JLabel[] labels = {};
        int[] tbl_widths_customers = {tf_fname.getWidth()};
        int width = 0;
        String[] col_names = {""};
        TableRenderer tr = new TableRenderer();
        TableRenderer.setPopup(tf_fname, obj, labels, tbl_widths_customers, col_names);
        tr.setCallback(new TableRenderer.Callback() {
            @Override
            public void ok(TableRenderer.OutputData data) {
                Field.Search tf = (Field.Search) tf_fname;
                Customers.to_customers to = customers.get(data.selected_row);
                tf_fname.setText(to.lname + ", " + to.fname + " " + to.mi);
                tf.setId("" + to.id);
                barangay = to.barangay;
                date_of_billing = DateType.slash.format(new Date());
                due_date = "";
                customer_name = to.lname + ", " + to.fname + " " + to.mi;
                tax_declaration_no = to.tax_dec_no;
                water_meter_no = to.meter_no;

                init_report();
            }
        });
    }

