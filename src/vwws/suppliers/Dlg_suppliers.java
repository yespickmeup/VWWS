    private void add_suppliers() {
        int id = 0;
        String supplier = tf_supplier.getText();
        String date_added = DateType.now();
        String date_updated = DateType.now();
        String added_by_id = MyUser.getUser_id();
        String update_by_id = MyUser.getUser_id();

        to_suppliers to = new to_suppliers(id, supplier, date_added, date_updated, added_by_id, update_by_id);
        Suppliers.add_data(to);
        tf_supplier.setText("");
        data_cols();
    }

